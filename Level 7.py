# Задача 1. Чтение и фильтрация строк
# Есть файл data.txt с содержимым:
# text
# Яблоко
# Банан
# Апельсин
# Груша
# Арбуз
# Задание:
# Открой файл в режиме чтения через with
# Выведи только строки, начинающиеся на букву "А" (регистр важен)
# Каждая найденная строка должна выводиться без лишних пробелов/переносов
# Требования:
# Используй with
# Проверяй начало строки методом startswith()
# Убирай лишние символы через strip()

with open("data.txt", "r", encoding="utf-8") as file:
    for line in file:
        if line.startswith("А"):
            print(line.strip())


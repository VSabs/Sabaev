# Задача 9 (Вложенные условия)
# Напиши код, который проверяет число score и выводит результат:
#
# "Отлично", если score >= 90,
#
# "Хорошо", если score между 70 и 89 включительно,
#
# "Удовлетворительно", если score между 50 и 69 включительно,
#

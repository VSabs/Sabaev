# Напиши код, который:
#
# Проверяет, является ли число x чётным (% 2 == 0),
#
# Положительным (> 0),
#
# Делится ли на 3 без остатка (% 3 == 0).
#
# Если все три условия верны, выведи "Число подходит".
# Если нет — "Число не подходит".

